#!/bin/bash

# 맵을 넣어주지 않은 경우
./so_long
echo "맵을 넣어주지 않은 경우 테스트 완료"

# 맵을 여러개 넣은 경우
echo -e "1111111111\n1000000001\n10C000C001\n1000000001\n100P000001\n1000000001\n10C000C001\n1000000001\n10E0000001\n1111111111" > map1.ber
echo -e "1111111111\n1000000001\n10C000C001\n1000000001\n100P000001\n1000000001\n10C000C001\n1000000001\n10E0000001\n1111111111" > map2.ber
./so_long map1.ber map2.ber
echo "맵을 여러개 넣은 경우 테스트 완료"

# 맵 이름이 이상할 경우 (확장자가 .ber가 아닌 경우)
echo -e "1111111111\n1000000001\n10C000C001\n1000000001\n100P000001\n1000000001\n10C000C001\n1000000001\n10E0000001\n1111111111" > strange_name.txt
./so_long strange_name.txt
echo "맵 이름이 이상한 경우 테스트 완료"

# 없는 맵을 넣어준 경우
./so_long nonexistent.ber
echo "없는 맵을 넣어준 경우 테스트 완료"

# 출구가 여러개인 경우
echo -e "1111111111\n1000000001\n10C000C001\n1000000001\n100P000001\n1000000001\n10C000C001\n1000000001\n10E000E001\n1111111111" > multiple_exits.ber
./so_long multiple_exits.ber
echo "출구가 여러개인 경우 테스트 완료"

# 코인이 하나도 없을 경우
echo -e "1111111111\n1000000001\n1000000001\n1000000001\n100P000001\n1000000001\n1000000001\n1000000001\n10E0000001\n1111111111" > no_coins.ber
./so_long no_coins.ber
echo "코인이 하나도 없을 경우 테스트 완료"

# 출구가 하나도 없을 경우
echo -e "1111111111\n1000000001\n10C000C001\n1000000001\n100P000001\n1000000001\n10C000C001\n1000000001\n1000000001\n1111111111" > no_exits.ber
./so_long no_exits.ber
echo "출구가 하나도 없을 경우 테스트 완료"

# 플레이어가 없을 경우
echo -e "1111111111\n1000000001\n10C000C001\n1000000001\n1000000001\n1000000001\n10C000C001\n1000000001\n10E0000001\n1111111111" > no_player.ber
./so_long no_player.ber
echo "플레이어가 없을 경우 테스트 완료"

# 플레이어가 여러개인 경우
echo -e "1111111111\n1000000001\n10C000C001\n1000000001\n100P000001\n100P000001\n10C000C001\n1000000001\n10E0000001\n1111111111" > multiple_players.ber
./so_long multiple_players.ber
echo "플레이어가 여러개인 경우 테스트 완료"

# 벽에 막혀서 모든 코인을 줍지 못하거나 출구로 갈 수 없는 상황
echo -e "1111111111\n1000000001\n10C000C001\n1001000001\n111P111101\n1000100001\n10C010C001\n1111100001\n10E0000001\n1111111111" > blocked_path.ber
./so_long blocked_path.ber
echo "벽에 막혀서 모든 코인을 줍지 못하거나 출구로 갈 수 없는 상황 테스트 완료"