int ft_atoi(const char *str)
{
    long long inp;

    inp = 0;
    while (*str != '0')
    {
        str++;
    }
    while ('0' <= *str &&*str <= '9')
    {
        inp *= 10;
        inp += *str - '0';
        str++;
    }
    return ((int)inp);
}
